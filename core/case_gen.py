# Анти-повтор: помним последнюю выбранную рамку по каждой оси, чтобы соседние дела
# одного сеттинга не дублировали место/жертву/конфликт. Живёт в памяти процесса.
_recent_frames: dict[str, dict[str, str]] = {}


def _choice_fresh(setting: str, axis: str, options: list[str]) -> str:
    """random.choice, избегающий значения, выбранного для этой оси в прошлый раз."""
    last = _recent_frames.get(setting, {}).get(axis)
    pool = [o for o in options if o != last] or options
    value = random.choice(pool)
    _recent_frames.setdefault(setting, {})[axis] = value
    return value


def _build_case_frame(setting: str) -> str:
    frame = _SETTING_FRAMES.get(setting, _SETTING_FRAMES["classic"])
    place = _choice_fresh(setting, "place", frame["place"])
    victim = _choice_fresh(setting, "victim", frame["victim"])
    conflict = _choice_fresh(setting, "conflict", frame["conflict"])
    tone = _choice_fresh(setting, "tone", frame["tone"])
    clues = _choice_fresh(setting, "clues", frame["clues"])
    return (
        f"Рамка дела:\n"
        f"- место действия: {place}\n"
        f"- тип жертвы: {victim}\n"
        f"- центральный конфликт: {conflict}\n"
        f"- эмоциональный тон: {tone}\n"
        f"- желаемый тип улик: {clues}\n"
        f"- избегай шаблонов и самоповторов; не своди дело к самой очевидной комбинации жанровых клише\n"
        f"- сделай локации, социальные роли, мотивы и детали визуально отличимыми от типового дела этого сеттинга"
    )


async def generate_case(setting: str, twist: str | None = None) -> dict:
    """Сгенерировать дело для выбранной вселенной.

    twist — ключ из _TWISTS; None -> случайный. Без LLM-ключа -> демо-дело.
    """
    if not config.llm_enabled:
        log.warning("LLM не настроен — отдаю демо-дело (sample).")
        return get_sample_case()

    setting_desc = _SETTINGS_RU.get(setting, _SETTINGS_RU["classic"])
    twist = twist or _pick_twist()
    twist_desc = _TWISTS.get(twist, _TWISTS["none"])
    case_frame = _build_case_frame(setting)
    # структурная вариативность: размер дела гуляет, чтобы «скелет» не был всегда одинаковым
    n_suspects = random.choice([3, 4, 4, 4, 5])
    n_locations = random.choice([3, 3, 4])
    seeds = random.sample(_UNIQUE_SEEDS, k=2)
    structure = (f"Структура этого дела: ровно {n_suspects} подозреваемых (убийца — один) "
                 f"и {n_locations} локации.\n"
                 f"Уникальные требования к этому делу (ОБЯЗАТЕЛЬНО встрой каждое):\n- "
                 + "\n- ".join(seeds))
    for attempt in range(3):
        try:
            case = await llm.ask_json(
                system=load("case_gen"),
                user=(f"Сеттинг: {setting_desc}.\n"
                      f"{case_frame}\n"
                      f"{structure}\n"
                      f"ТВИСТ ДЕЛА: {twist_desc}\n"
                      f"Встрой твист в сюжет, улики и solution_chain. Сгенерируй дело строго по схеме JSON."),
            )
            case["id"] = uuid.uuid4().hex
            case["setting"] = setting
            case["twist"] = twist
            if await validate_case(case):
                return case
            log.warning("Дело не прошло валидацию (попытка %s), регенерирую.", attempt + 1)
        except Exception as e:  # noqa: BLE001
            log.exception("Ошибка генерации дела (попытка %s): %s", attempt + 1, e)

    log.error("Не удалось сгенерировать валидное дело — фолбэк на демо.")
    return get_sample_case()


async def validate_case(case: dict) -> bool:
    """Проверка решаемости (вызов B). Базовые структурные проверки + LLM-аудит логики."""
    # дешёвые структурные проверки до обращения к LLM
    try:
        suspects = case["suspects"]
        murderer = case["murderer_id"]
        if not (3 <= len(suspects) <= 6):
            return False
        if murderer not in {s["id"] for s in suspects}:
            return False
        if not case.get("clues") or not case.get("solution_chain"):
            return False
        for clue in case["clues"]:
            if "read_text" in clue and not str(clue["read_text"]).strip():
                return False
            if clue.get("is_false_lead") and not clue.get("refuted_by"):
                return False
    except (KeyError, TypeError):
        return False

    if not config.llm_enabled:
        return True

    verdict = await llm.ask_json(
        system=load("validator"),
        user=f"Дело: {case}",
        temperature=0.0,
    )
    return bool(verdict.get("solvable"))
